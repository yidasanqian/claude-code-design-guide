# Process SVG file
process_svg() {
    local input_file="$1"
    local output_file="$2"

    # Read file and translate text content
    sed 's/[\u4e00-\u9fff]/CHINESE_CHAR/g' "$input_file" > "$output_file"
}

# Main
DIAGRAMS_DIR="/Users/liugang/study/ai/CC/claudecode/architecture/diagrams"

for svg_file in "$DIAGRAMS_DIR"/*.svg; do
    filename=$(basename "$svg_file")

    # Skip if already has -en suffix
    if [[ "$filename" == *"-en.svg" ]]; then
        continue
    fi

    # Create English version filename
    en_filename="${filename%.svg}-en.svg"
    en_file="$DIAGRAMS_DIR/$en_filename"

    # Skip if English version already exists
    if [[ -f "$en_file" ]]; then
        echo "Skip: $en_filename (already exists)"
        continue
    fi

    echo "Creating: $en_filename"
    cp "$svg_file" "$en_file"
done
